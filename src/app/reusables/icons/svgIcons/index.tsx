export default function svgIcons ( iconName ) {
    switch ( iconName ) {
        case 'fire':
            return '🔥';
        case 'fistbump':
            return '👊';
        case 'ogre':
            return '👹';
        case 'mountain':
            return '🗻'
        case 'sunrise':
            return '🌄';
        case 'heart':
            return '❤️️';
        case 'blueHeart':
            return '💙';
        default:
            return '🗻'
    }
}
